class TestGenerateCnpjReportLlmFallback:
    """Ensure PDF generation survives LLM failures."""

    def test_llm_exception_does_not_break_pdf(self):
        """If generate_cnpj_narrative raises an Exception, PDF is still generated."""
        from pdf_generator_intel_report import generate_cnpj_report

        # Build data without a 'narrative' key — simulating caller that would
        # normally inject the narrative from LLM. Even without narrative, the
        # PDF generator must not crash.
        data = {**MINIMAL_DATA}
        data.pop("narrative", None)

        result = generate_cnpj_report(data)
        content = result.getvalue()
        assert content[:4] == b"%PDF", "PDF must still be valid when narrative is absent"

    def test_generate_cnpj_narrative_falls_back_on_openai_exception(self):
        """generate_cnpj_narrative returns deterministic fallback when OpenAI raises."""
        from llm import generate_cnpj_narrative

        with patch("llm.OpenAI") as mock_openai_cls:
            mock_client = MagicMock()
            mock_openai_cls.return_value = mock_client
            mock_client.chat.completions.create.side_effect = Exception("API unavailable")

            # Ensure OPENAI_API_KEY is set so we reach the OpenAI call
            with patch.dict("os.environ", {"OPENAI_API_KEY": "test-key"}):
                result = generate_cnpj_narrative(MINIMAL_DATA)

        _assert_narrative_structure(result)

    def test_generate_cnpj_narrative_falls_back_when_no_api_key(self):
        """generate_cnpj_narrative returns deterministic fallback when API key is absent."""
        from llm import generate_cnpj_narrative

        with patch.dict("os.environ", {}, clear=True):
            # Remove OPENAI_API_KEY if present
            import os
            env_without_key = {k: v for k, v in os.environ.items() if k != "OPENAI_API_KEY"}
            with patch.dict("os.environ", env_without_key, clear=True):
                result = generate_cnpj_narrative(MINIMAL_DATA)

        _assert_narrative_structure(result)


