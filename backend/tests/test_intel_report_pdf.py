# Tests: generate_cnpj_report
# ---------------------------------------------------------------------------

class TestGenerateCnpjReport:
    """Tests for the PDF generator function."""

    def test_valid_data_returns_bytesio_with_pdf_magic_bytes(self):
        """PDF is generated correctly from complete data."""
        from pdf_generator_intel_report import generate_cnpj_report

        result = generate_cnpj_report(MINIMAL_DATA)

        assert isinstance(result, BytesIO)
        content = result.getvalue()
        assert len(content) > 0, "PDF must not be empty"
        assert content[:4] == b"%PDF", f"PDF must start with %PDF magic bytes, got {content[:4]!r}"

    def test_pdf_contains_reportlab_marker(self):
        """Generated bytes contain ReportLab marker (sanity check)."""
        from pdf_generator_intel_report import generate_cnpj_report

        result = generate_cnpj_report(MINIMAL_DATA)
        content = result.getvalue()
        # ReportLab always writes the PDF header comment
        assert b"%PDF-" in content

    def test_empty_dict_raises_value_error(self):
        """Empty dict raises ValueError."""
        from pdf_generator_intel_report import generate_cnpj_report

        with pytest.raises(ValueError):
            generate_cnpj_report({})

    def test_none_raises_value_error(self):
        """None input raises ValueError."""
        from pdf_generator_intel_report import generate_cnpj_report

        with pytest.raises(ValueError):
            generate_cnpj_report(None)  # type: ignore[arg-type]

    def test_minimal_data_without_optional_sections(self):
        """PDF generated even when optional sections (narrative, orgaos, etc.) are absent."""
        from pdf_generator_intel_report import generate_cnpj_report

        sparse = {
            "cnpj": "99.888.777/0001-00",
            "fornecedor_nome": "Empresa Vazia LTDA",
            "total_contratos": 0,
            "valor_total": 0.0,
        }
        result = generate_cnpj_report(sparse)
        content = result.getvalue()
        assert content[:4] == b"%PDF"
        assert len(content) > 500

    def test_no_narrative_field_uses_deterministic_fallback(self):
        """When 'narrative' key is missing, PDF is still generated via deterministic fallback."""
        from pdf_generator_intel_report import generate_cnpj_report

        data_no_narrative = {k: v for k, v in MINIMAL_DATA.items() if k != "narrative"}
        result = generate_cnpj_report(data_no_narrative)
        content = result.getvalue()
        assert content[:4] == b"%PDF"

    def test_bytesio_seeked_to_start(self):
        """Returned BytesIO is positioned at the start (tell() == 0)."""
        from pdf_generator_intel_report import generate_cnpj_report

        result = generate_cnpj_report(MINIMAL_DATA)
        assert result.tell() == 0


