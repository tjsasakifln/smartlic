class TestGenerateCnpjNarrative:
    """Tests for the LLM narrative generation function."""

    def test_fallback_returns_all_required_keys(self):
        """Deterministic fallback returns dict with all 4 required keys."""
        from llm import _generate_cnpj_narrative_fallback

        result = _generate_cnpj_narrative_fallback(MINIMAL_DATA)
        _assert_narrative_structure(result)

    def test_fallback_with_empty_data(self):
        """Deterministic fallback handles empty data dict gracefully."""
        from llm import _generate_cnpj_narrative_fallback

        result = _generate_cnpj_narrative_fallback({})
        _assert_narrative_structure(result)
        assert result["padrao_competitivo"]  # must have some text

    def test_fallback_includes_total_contratos(self):
        """Fallback text mentions the total contracts count."""
        from llm import _generate_cnpj_narrative_fallback

        result = _generate_cnpj_narrative_fallback(MINIMAL_DATA)
        assert "5" in result["padrao_competitivo"]

    def test_llm_path_happy(self):
        """LLM path parses JSON response correctly when OpenAI returns valid JSON."""
        from llm import _generate_cnpj_narrative_llm

        fake_json = {
            "padrao_competitivo": "Padrão X",
            "principais_clientes": "Cliente A",
            "setores_foco": "TI",
            "pontos_atencao": "Atenção Y",
        }
        import json as _json

        mock_choice = MagicMock()
        mock_choice.message.content = _json.dumps(fake_json)

        mock_response = MagicMock()
        mock_response.choices = [mock_choice]

        with patch("llm.OpenAI") as mock_openai_cls:
            # mock_openai_cls() returns the mock client instance
            mock_openai_cls.return_value.chat.completions.create.return_value = mock_response

            with patch.dict("os.environ", {"OPENAI_API_KEY": "test-key"}):
                result = _generate_cnpj_narrative_llm(MINIMAL_DATA)

        assert result["padrao_competitivo"] == "Padrão X"
        assert result["principais_clientes"] == "Cliente A"
        assert result["setores_foco"] == "TI"
        assert result["pontos_atencao"] == "Atenção Y"


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _assert_narrative_structure(result: dict) -> None:
    """Assert narrative dict has all 4 required string keys."""
    assert isinstance(result, dict), f"Expected dict, got {type(result)}"
    for key in ("padrao_competitivo", "principais_clientes", "setores_foco", "pontos_atencao"):
        assert key in result, f"Missing key: {key}"
        assert isinstance(result[key], str), f"Key {key} must be str, got {type(result[key])}"
